#import "UXRTableViewController.h"
#import "UXRYelpNetworkingEngine.h"

@interface UXRTableViewController ()
@property(nonatomic,strong) UXRYelpNetworkingEngine *yelpEngine;
@property(nonatomic,strong) NSArray *restaurants;
@end

@implementation UXRTableViewController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    self.restaurants = @[];
    self.yelpEngine = [[UXRYelpNetworkingEngine alloc] init];
}

-(void)viewDidAppear:(BOOL)animated{
    [super viewDidAppear:animated];
    
    [self.yelpEngine getRestaurantsWithCompletionBlock:^(NSArray *restaurants) {
        self.restaurants = restaurants;
        [self.tableView performSelectorOnMainThread:@selector(reloadData) withObject:nil waitUntilDone:NO];
    } failureBlock:^(NSError *error) {
        NSLog(@"Failed to get restaurants, check your API credentials and try again");
    }];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return self.restaurants.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    NSDictionary *restaurant = self.restaurants[indexPath.row];
    // Configure the cell...
    cell.textLabel.text = restaurant[@"name"];
    
    return cell;
}

